#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*str;

	len = ft_strlen(s) + 1;
	str = (char *) malloc(len);
	if (str == 0)
		return (NULL);
	ft_strlcpy(str, s, len);
	return (str);
}
